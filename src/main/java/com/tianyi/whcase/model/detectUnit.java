package com.tianyi.whcase.model;

public class detectUnit {
	private int id;
	private int detectedunitId;
	private String detectedunitName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getDetectedunitId() {
		return detectedunitId;
	}
	public void setDetectedunitId(int detectedunitId) {
		this.detectedunitId = detectedunitId;
	}
	public String getDetectedunitName() {
		return detectedunitName;
	}
	public void setDetectedunitName(String detectedunitName) {
		this.detectedunitName = detectedunitName;
	}
	
}
