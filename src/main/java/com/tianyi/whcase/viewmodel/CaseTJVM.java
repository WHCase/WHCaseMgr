package com.tianyi.whcase.viewmodel;

public class CaseTJVM {
	/*
	 * 案件总数
	 */
	private int caseTotalCount;
	/*
	 * 已分配案件数量
	 */
	private int distributedCaseCount;
	/*
	 * 未分配案件数量
	 */
	private int notDistributeCaseCount;
	/*
	 * 已接收案件数量
	 */
	private int receivedCaseCount;
	/*
	 * 未接收案件数量
	 */
	private int notReceivedCaseCount;
	/*
	 * 已反馈案件数量
	 */
	private int feedBackCaseCount;
	/*
	 * 未反馈案件数量
	 */
	private int notFeedBackCaseCount;
	public int getCaseTotalCount() {
		return caseTotalCount;
	}
	public void setCaseTotalCount(int caseTotalCount) {
		this.caseTotalCount = caseTotalCount;
	}
	public int getDistributedCaseCount() {
		return distributedCaseCount;
	}
	public void setDistributedCaseCount(int distributedCaseCount) {
		this.distributedCaseCount = distributedCaseCount;
	}
	public int getNotDistributeCaseCount() {
		return notDistributeCaseCount;
	}
	public void setNotDistributeCaseCount(int notDistributeCaseCount) {
		this.notDistributeCaseCount = notDistributeCaseCount;
	}
	public int getReceivedCaseCount() {
		return receivedCaseCount;
	}
	public void setReceivedCaseCount(int receivedCaseCount) {
		this.receivedCaseCount = receivedCaseCount;
	}
	public int getNotReceivedCaseCount() {
		return notReceivedCaseCount;
	}
	public void setNotReceivedCaseCount(int notReceivedCaseCount) {
		this.notReceivedCaseCount = notReceivedCaseCount;
	}
	public int getFeedBackCaseCount() {
		return feedBackCaseCount;
	}
	public void setFeedBackCaseCount(int feedBackCaseCount) {
		this.feedBackCaseCount = feedBackCaseCount;
	}
	public int getNotFeedBackCaseCount() {
		return notFeedBackCaseCount;
	}
	public void setNotFeedBackCaseCount(int notFeedBackCaseCount) {
		this.notFeedBackCaseCount = notFeedBackCaseCount;
	}
	
}
